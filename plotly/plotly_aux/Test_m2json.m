classdef Test_m2json < matlab.unittest.TestCase
    methods (Test)
        function testMain(tc)
            tb = cell2table( ...
                {1 +      (1:5)+0.23456789      "in range [0,10]"
                1e-6*(1 + (1:5)+0.23456789)     "in range [1e-6,1e-5]"
                1e14 +    (1:5)+0.23456789      "in range 1e14+[0,1]"
                1e14 + 1e7 + (1:5)+0.23456789   "in range 1e14+1e7+[0,1]"
                1e14 + 10.^(1:5)+0.23456789     "log scaled variables"
                -(1 +      (1:5)+0.23456789)    "in range [-10,0]"
                -1e-6*(1 + (1:5)+0.23456789)    "in range [-1e-5,-1e-6]"
                -1e14 +    (1:5)+0.23456789     "in range -1e14+[0,1]"},VariableNames=["values"  "label"]);
            exp = ["[2.235,3.235,4.235,5.235,6.235]"
                "[2.235e-06,3.235e-06,4.235e-06,5.235e-06,6.235e-06]"
                "[100000000000001,100000000000002,100000000000003,100000000000004,100000000000005]"
                "[100000010000001,100000010000002,100000010000003,100000010000004,100000010000005]"
                "[1e+14,1.000000000001e+14,1.00000000001e+14,1.0000000001e+14,1.000000001e+14]"
                "[-2.235,-3.235,-4.235,-5.235,-6.235]"
                "[-2.235e-06,-3.235e-06,-4.235e-06,-5.235e-06,-6.235e-06]"
                "[-99999999999998.8,-99999999999997.8,-99999999999996.8,-99999999999995.8,-99999999999994.8]"];
            vals = arrayfun(@(i)string(m2json(tb.values(i,:))),(1:height(tb))',un=0);
            tc.verifyEqual(vertcat(vals{:}),exp);
        end
    end
end
